// Verificando número

function verificarNumero(num) {
    if(num > 0){
        console.log("O numero é positivo");
    } else if (num < 0){
        console.log("O número é negativo");
    } else {
        console.log("O número é zero");
    }
}
verificarNumero(0);
verificarNumero(5);
verificarNumero(-9);